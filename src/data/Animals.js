//all filter function
export function getVisibleAnimals(types=[],colors=[],breeds=[]){
    let filteredAnimals = Animals.filter((animal)=>{
        let setType = true
        if(types.length)
            setType = types.includes(animal.type)
        let setColors = true
        if(colors.length){
            let arry = animal.colors.primary.map(clr=>{
                return colors.includes(clr)
            })
            setColors = arry.includes(true)
        }
        let setBreeds = true
        if(breeds.length){
            setBreeds = breeds.includes(animal.breeds.primary)
        }
        return setType && setColors && setBreeds
    })

    return filteredAnimals
}